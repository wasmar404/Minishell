#include "builtins.h"

void	ft_atoll_helper(const char *nptr, int *i, int *sign)
{
	while (nptr[(*i)] == 32 || (nptr[(*i)] >= 9 && nptr[(*i)] <= 13))
		(*i)++;
	if (nptr[(*i)] == '-' || nptr[(*i)] == '+')
	{
		if (nptr[(*i)] == '-')
			(*sign) = -1;
		(*i)++;
	}
}
